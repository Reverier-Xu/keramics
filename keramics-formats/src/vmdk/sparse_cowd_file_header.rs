/// VMware Virtual Disk (VMDK) sparse Copy-On-Write Disk (COWD) file header.
pub struct VmdkSparseCowdFileHeader {
    /// Sectors per grain.
    pub sectors_per_grain: u32,

    /// Grain directory start sector.
    pub grain_directory_start_sector: u32,

    /// Number of grain directory entries.
    pub number_of_grain_directory_entries: u32,
}

impl VmdkSparseCowdFileHeader {
    /// Creates a new file header.
    pub fn new() -> Self {
        Self {
            sectors_per_grain: 0,
            grain_directory_start_sector: 0,
            number_of_grain_directory_entries: 0,
        }
    }

    /// Reads the file header from a buffer.
    pub fn read_data(&mut self, data: &[u8]) -> Result<(), ErrorTrace> {
        if data.len() != 2048 {
            return Err(keramics_core::error_trace_new!(
                "Unsupported file header data size"
            ));
        }
        if &data[0..4] != VMDK_SPARSE_COWD_FILE_HEADER_SIGNATURE {
            return Err(keramics_core::error_trace_new!(
                "Unsupported file header signature"
            ));
        }
        self.sectors_per_grain = bytes_to_u32_le!(data, 16);
        self.grain_directory_start_sector = bytes_to_u32_le!(data, 20);
        self.number_of_grain_directory_entries = bytes_to_u32_le!(data, 24);

        Ok(())
    }
}

