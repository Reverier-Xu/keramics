#[test]
fn decode() -> Result<(), ErrorTrace> {
    for (expected_code_points, test_byte_string) in MAC_INUIT_TEST_VECTOR.iter() {
        let mut decoder: DecoderMacInuit = DecoderMacInuit::new(test_byte_string);

        let test_code_points: Vec<u32> = match decoder.next() {
            Some(Ok(code_points)) => code_points,
            Some(Err(error)) => return Err(error),
            None => {
                return Err(keramics_core::error_trace_new!(format!(
                    "Failed to decode MacInuit as code_point: U+{:04x}",
                    expected_code_points[0]
                )));
            }
        };
        assert_eq!(&test_code_points, expected_code_points);
    }
    Ok(())
}

#[test]
fn encode() -> Result<(), ErrorTrace> {
    for (test_code_points, expected_byte_string) in MAC_INUIT_TEST_VECTOR.iter() {
        let mut encoder: EncoderMacInuit = EncoderMacInuit::new(test_code_points);

        let test_byte_string: Vec<u8> = match encoder.next() {
            Some(Ok(byte_string)) => byte_string,
            Some(Err(error)) => return Err(error),
            None => {
                return Err(keramics_core::error_trace_new!(format!(
                    "Failed to encode code point: U+{:04x} as MacInuit",
                    test_code_points[0]
                )));
            }
        };
        assert_eq!(&test_byte_string, expected_byte_string);
    }
    Ok(())
}
